//Arithmetic operators
fun main(){
    var num1:Int=25
    var num2:Int=6
    println(num1 + num2)
    println(num1 - num2)
    println(num1 * num2)
    println(num1 / num2)
    println(num1 % num2)

    //Assignment operators
    var x=16
    println(x)

    var y=5
    y+=5
    println(y)

    var a=4
    a*=4
    println(a)

    //Comparison operators

    var number:Int=100
    println(number<20)
    println(number>10)
    println(number<=40)
    println(number>=40)
    println(number!=40)

    //Logical operators

    var rnum=90
    println(rnum<10 && rnum>100)
    println(!(rnum<10 || rnum>10))
    println(rnum<10 || rnum>10)

}