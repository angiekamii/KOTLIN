fun main(){
    sum(32,12)
    sub(43,21)
    multi(4,5)
    division(24,6)

}

fun sum(num1:Int,num2:Int){
    println(num1+num2)
}
fun sub(num1:Int, num2:Int){
    println(num1 - num2)
}
fun multi(num1: Int, num2: Int){
    println(num1 * num2)
}
fun division(num1: Int, num2: Int){
    println(num1 / num2)
}
